export const ImageWithFallback = ({ src, alt, className, symbol }) => {
  const [error, setError] = useState(false);
  const handleImageError = () => setError(true);
  useEffect(() => setError(false), [src]);

  if (error || !src) {
    return (
      <span
        className={`${className} flex items-center justify-center bg-[#2A2A2A] text-[#848E9C] font-bold text-xs rounded-full`}
      >
        {symbol?.substring(0, 1)?.toUpperCase() || "?"}
      </span>
    );
  }
  return (
    <img src={src} alt={alt} className={className} onError={handleImageError} />
  );
};
